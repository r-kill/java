public class PrePostIncrement {

    public static void main(String[] args) {
        int c = 5;

        System.out.println(c);
        System.out.println(c++);
        System.out.println(c);

        System.out.println();

        c = 5;
        System.out.println(c);
        System.out.println(++c);
        System.out.println(c);
    }//end Main
}//end Class
